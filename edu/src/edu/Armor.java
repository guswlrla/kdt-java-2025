package edu;

public class Armor {
	private String name;
	int height;

	public String getName() { // getter - 게터라고 부른다
		return name;
	}

	public void setName(String value) { // setter - 세터라고 부른다
		name = value;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int value) {
		height = value;
	}
}
