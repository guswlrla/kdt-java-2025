package com.ruby.java.ch02데이터타입;

public class Test08 {
	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		
		System.out.println(a > b); // false
		System.out.println(a < b); // true
		System.out.println(a >= b); // false
		System.out.println(a <= b); // true
		System.out.println(a == b); // false
		System.out.println(a != b); // true
	}
}

